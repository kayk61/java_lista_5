package lista_5_java;

public class at10 {
    public static void main(String[] args) {
        //Tabuada Fixa: Exiba a tabuada de multiplicação do número 7 (de 1 a 10).//
        int n, c;
        n = 7;
        c = 0;

        while (c < 10) {
            c = c + 1;

            System.out.println(n*c);
        }
    }
}
